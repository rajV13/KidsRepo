userGrade = input("What grade you got? : ").upper()

if userGrade == "A" :
    print("\nFirst Division")
elif userGrade == "B" :
    print ("\nSecond Division")
elif userGrade == "C" :
    print ("\nThird Division")
else :
    print ("\nTry hard to get a good grade !")

print ("\nHave a nice day ! ")